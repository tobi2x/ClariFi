// light and dark mode
function backgroundColor() {
    var body = document.body;
    var checkbox = document.getElementById('modeSwitch');

    checkbox.addEventListener('change', function() {
        if (this.checked) {
            body.classList.add('dark-mode');
        } else {
            body.classList.remove('dark-mode');
        }
    });
}

// Call the function when the DOM content is loaded
document.addEventListener("DOMContentLoaded", backgroundColor);

// change text size
document.getElementById("slider").addEventListener("input", function() {
    var fontSize = this.value + "px";
    document.getElementById("name").style.fontSize = fontSize;
});

// change button color
document.getElementById('colorChangeButton').addEventListener('click', function() {
    this.classList.toggle('changed-color');
  });

